  <h2>Lucombe Oak</h2>
  <p>Keeping watch over farmyard activities, this large uncommon tree, a hybrid of Turkey Oak and Cork Oak, was first noted by William Lucombe, an Exeter nurseryman, in the 1760s. It has mossy acorn cups and pointy lobes on its leaves like the Turkey Oak and keeps its leaves throughout the winter until just before new growth appears, like its other parent, the Cork Oak.   Legend has it that Lucombe was so impressed with the timber that he that he felled his original tree in 1785 and kept planks of it under his bed to make a coffin.  By the time he died at the age of 102, his timber had rotted beyond use so another tree was felled and used instead.</p>
