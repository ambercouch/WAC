  <h2>World War Two British Resistance Hideout</h2>
  <p>Will you be able to find our secret underground hide-out? Located near a path that runs through our bluebell woodland, it was built by the British Resistance Movement during World War II, the world’s first resistance organization ever to be established in advance of an invasion, and widely regarded as the forerunners of today’s SAS.</p>
<p>At this time, an invasion of Britain was considered imminent so Churchill ordered that small teams of local men who know their countryside intimately were recruited secretly from the Home Guard and given specialist training in sabotage and guerrilla warfare.  The Units were organised in patrols of between four and eight men, with each patrol having a specially constructed Operational Base (also called a hide or hide-out), usually hidden in woodland.  The hideout at Plas Farm is one of very few to have survived in Britain and is listed by the government as a Scheduled Ancient Monument in recognition of its national importance as a significant relic from the Second World War. </p>
</div>

