  <h2>Historic Field Boundary</h2>
  <p>A characteristic feature of the Welsh landscape - an earth bank faced with vertical stones, or clawdd, upon which sits a modern stock proof barbed wire fence. In Wales, most field boundaries are less than 200 years old, making them a relatively new addition to the landscape.  Very often a clawdd will have (or will have had) a hedge on top of it.  It is likely that the three hawthorns and silver birch in the picture are the remains of an old hedge. The earth core can be a haven for small mammals – more so than a dry stone wall.  Something in excess of 80% of all the recorded flowering plant species in Wales have been found on cloddiau (plural of clawdd).</p>
  <p>The clawdd is most common in cattle areas where there is less need for height in terms of stock proofing. If additional stock proofing was required dead wood or gorse would have been piled on top of the structure.  Luckily for our generation, in 1867, Lucien B. Smith of Kent, Ohio developed the first wire technology capable of restraining cattle.  He had invented barbed wire.  In 1874, a Wild West farmer called Joseph Glidden improved his design and started to produce barbed wire in the form we recognize today. Over a century later and we still haven’t bettered his idea.</p>
</div>

